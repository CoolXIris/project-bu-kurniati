                <p class="text-white-300 font-bold mb-3" style="font-size: 20px;">1 . Buka Pengaturan</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Akses menu Pengaturan pada perangkat Android Anda. Menu ini biasanya dapat diakses melalui ikon roda gigi di layar utama atau di panel notifikasi.</p>

