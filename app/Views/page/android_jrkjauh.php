                <p class="text-white-300 mb-4" style="text-align: justify;">Setelah melakukan reset perangkat, ada beberapa langkah tambahan yang perlu Anda lakukan untuk menjaga keamanan akun dan data pribadi Anda:</p>

                <p class="text-white-300 font-bold mb-3" style="font-size: 20px;">1 . Ganti Kata Sandi Akun Google</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Untuk mencegah akses yang tidak sah ke akun Anda, segera ganti kata sandi akun Google setelah melakukan reset. Ini memastikan bahwa siapa pun yang mungkin mendapatkan akses ke HP tidak dapat login ke akun Anda.</p>

                <p class="text-white-300 font-bold mb-3" style="font-size: 20px;">2 . Laporkan Kehilangan ke Operator Seluler</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Selain mereset perangkat, Anda juga sebaiknya melaporkan kehilangan ponsel ke operator seluler. Mereka dapat membantu Anda memblokir kartu SIM untuk mencegah penggunaan yang tidak sah.</p>

                <p class="text-white-300 font-bold mb-3" style="font-size: 20px;">3 . Aktifkan Verifikasi Dua Langkah</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Untuk menambah lapisan keamanan, aktifkan verifikasi dua langkah di akun Google Anda. Ini akan membantu mencegah akses yang tidak sah ke akun meskipun seseorang memiliki kata sandi Anda.</p>

