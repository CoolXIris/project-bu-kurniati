                <p class="text-white-300 mb-4" style="text-align: justify;">Di dalam pengaturan, cari dan pilih opsi Keamanan atau Privasi. Di beberapa versi Android, Anda mungkin perlu mencari opsi Google terlebih dahulu, kemudian pilih Find My Device.</p>

                <p class="text-white-300 font-bold mb-3" style="font-size: 20px;">3 . Aktifkan Find My Device</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Pastikan opsi Find My Device aktif. Ini memungkinkan perangkat Anda dilacak dan dikontrol dari jarak jauh melalui akun Google.</p>

                <p class="text-white-300 mb-4" style="text-align: justify;">Selain itu, pastikan juga bahwa ponsel terhubung ke akun Google, memiliki akses internet, dan lokasi GPS diaktifkan agar proses pelacakan dapat berjalan dengan baik.</p>

                <p class="text-white-300 font-bold mb-3" style="font-size: 20px;">Cara Reset HP Android dari Jarak Jauh</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Setelah memastikan bahwa Find My Device telah aktif, Anda bisa melakukan reset jarak jauh kapan saja jika perangkat Anda hilang atau dicuri. Langkah-langkah berikut dapat Anda lakukan melalui komputer, laptop, atau perangkat mobile lainnya:</p>


                <p class="text-white-300 font-bold mb-3" style="font-size: 20px;">1 . Akses Find My Device</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Buka Find My Device di browser perangkat lain. Anda juga bisa mengunduh aplikasi Find My Device di ponsel Android lain.</p>

                <p class="text-white-300 font-bold mb-3" style="font-size: 20px;">2 . Masuk dengan Akun Google</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Masuk ke akun Google yang sama dengan yang digunakan di HP yang hilang. Pastikan Anda memasukkan kredensial yang benar untuk mendapatkan akses ke perangkat yang hilang.</p>

                <p class="text-white-300 font-bold mb-3" style="font-size: 20px;">3 . Lacak Perangkat</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Setelah masuk, situs akan menampilkan daftar perangkat yang terhubung ke akun Google Anda. Pilih perangkat yang ingin Anda reset. Pada peta, Anda akan melihat lokasi terakhir perangkat jika masih terhubung dengan internet dan GPS.</p>

                <p class="text-white-300 font-bold mb-3" style="font-size: 20px;">4 . Pilih Opsi “Erase Device”</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Setelah perangkat terdeteksi, pilih opsi Erase Device atau Hapus Perangkat. Dengan memilih opsi ini, Anda akan menghapus semua data di perangkat secara permanen, termasuk foto, video, aplikasi, dan pengaturan. Perlu diingat bahwa setelah proses ini, Anda tidak bisa melacak perangkat lagi.</p>

                <p class="text-white-300 font-bold mb-3" style="font-size: 20px;">5 . Konfirmasi Penghapusan</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Sistem akan meminta konfirmasi sebelum melakukan penghapusan data. Pastikan Anda yakin sebelum melanjutkan karena data yang dihapus tidak bisa dipulihkan.</p>

