                <p class="text-white-300 font-bold mb-4" style="font-size:20px">Desain</p>
                <img alt="Gaming setup with a monitor displaying a game, a gaming PC with RGB lighting, and gaming accessories" class="w-full mb-4" height="400" src="<?= base_url('css/img/d_ipadm2.png') ?>" style="width: auto;height:auto;border-radius:2%;object-fit:content" />
                <p class="text-white-300 mb-4" style="text-align: justify;">Sama seperti generasi sebelumnya, sedikit lebih membosankan. Kenapa begitu? Karena pilihan warna iPad Air M2 kini dibuat lebih sedikit, yakni hanya Space Gray, Starlight, Purple, dan Blue seperti yang sedang saya ulas kali ini. Oh ya, sesuai judul, ini adalah varian 13 inci ya. Tentu lebih besar dari generasi sebelumnya yang hanya tersedia dalam ukuran layar 11 inci.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Untuk sebuah tablet berdimensi layar besar, sejatinya secara keseluruhan tergolong cukup ringan dalam bobot 617 gram, serta ketebalan hanya 6,1mm saja. Bodinya lebih mengotak, berbeda dengan, misalnya, Galaxy Tab S9+ yang lebih lebar. Form factor mendekati kotak seperti iPad Air M2 membuatnya lebih mudah masuk ke berbagai jenis kantong/tas, serta tidak merasa memegang perangkat yang terlalu tinggi saat dalam posisi portrait.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Bodinya sendiri menggunakan material aluminium, dengan kaca depan yang diperkuat oleh lapisan tahan gores. Secara fisik, untuk saya pribadi, versi 13 inci ini terasa sangat besar. Terutama sebagai pengganti laptop, ketika dipasangkan bersama Magic Keyboard, dimensinya lebih tebal dan lebih berat dari MacBook Air M2 yang biasa saya gunakan—terasa sedikit kurang portabel.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Namun bila friends memang bisa sepenuhnya meninggalkan laptop dan cukup dengan iPad Air M2, tentu menjadi opsi menggoda karena layarnya yang besar dan fleksibel, alias bisa digunakan atau dibawa sebagai tablet saja, atau dengan aksesori lainnya termasuk keyboard.</p>

                <p class="text-white-300 font-bold mb-4" style="font-size:20px">Layar</p>
                <img alt="Gaming setup with a monitor displaying a game, a gaming PC with RGB lighting, and gaming accessories" class="w-full mb-4" height="400" src="<?= base_url('css/img/l_ipadm2.png') ?>" style="width: auto;height:auto;border-radius:2%;object-fit:content" />
                <p class="text-white-300 mb-4" style="text-align: justify;">Untuk pertama kalinya, iPad Air M2 tersedia dalam dua ukuran layar berbeda, yakni 11 inci dan 13 inci seperti yang sedang saya ulas kali ini. Merupakan kabar baik terutama bagi mereka yang ingin membeli tablet iPad layar besar, tetapi tidak membutuhkan kecanggihan dari iPad Pro. Walaupun memang, panel layar yang digunakan berbeda.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Bukan Tandem OLED seperti iPad Pro M4, bukan pula miniLED seperti iPad Pro sebelumnya. Panel layar iPad Air M2 masih berjenis IPS, punya refresh rate sebatas 60Hz, dengan kerapatan piksel cukup baik di 264 cukup baik di 264ppi. Setidaknya, tingkat kecerahan layarnya sudah membaik, mencapai 600 nits dan cukup untuk penggunaan siang hari di luar ruangan.</p>
                <img alt="Gaming setup with a monitor displaying a game, a gaming PC with RGB lighting, and gaming accessories" class="w-full mb-4" height="400" src="<?= base_url('css/img/l_ipadm2_1.png') ?>" style="width: auto;height:auto;border-radius:2%;object-fit:content" />
                <p class="text-white-300 mb-4" style="text-align: justify;">Saturasi dan kontras yang diberikan, menurut saya bisa bersaing dengan panel layar AMOLED, atau setidaknya jauh lebih baik dibandingkan panel IPS standar. Jadi tak perlu khawatir kalau visualnya tidak sebaik iPad Pro, karena tak begitu jauh bedanya. Dan seperti iPad Pro terbaru, layar iPad Air M2 kompatibel dengan Apple Pencil Pro.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Dijual secara terpisah, sisi bezel iPad Air M2 memiliki posisi magnet terbaru, membuatnya mendukung stylus baru sekaligus peletakkan kamera selfie yang kini lebih pas. Dukungan kompabilitas ini, saya rasa bakal sangat menggoda untuk mereka yang ingin menggambar/membuat sketsa/lainnya dari iPad, mencari opsi layar besar, tapi tak punya bujet untuk iPad Pro terbaru.</p>

                <p class="text-white-300 font-bold mb-4" style="font-size:20px">Kamera</p>
                <img alt="Gaming setup with a monitor displaying a game, a gaming PC with RGB lighting, and gaming accessories" class="w-full mb-4" height="400" src="<?= base_url('css/img/k_ipadm2.png') ?>" style="width: auto;height:auto;border-radius:2%;object-fit:content" />
                <p class="text-white-300 mb-4" style="text-align: justify;">Di sisi belakang, kamera iPad Air membawa sensor 12MP f/1.8 dengan PDAF. Kualitasnya terasa seperti sedang menggunakan kamera iPhone SE terbaru, alias tergolong cukup berkualitas. Ditambah dengan algoritma Smart HDR dari chipset yang relatif baru. Lebih dari cukup untuk mengabadikan berbagai momen di belakang layar tablet.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Yang baru di iPad Air M2, adalah penempatan kamera selfie-nya, kini berada di sisi bezel atas ketika dalam posisi landscape. Dengan begitu, posisi wajah kini bisa lebih tepat. Tetap membawa sensor 12MP f/2.4 dengan sudut pandang yang sangat luas, mencapai 122 derajat. Tak perlu khawatir terlalu lebar, karena sudah mendukung fitur tracking wajah otomatis.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Kamera depan iPad Air M2 bisa merekam video dalam resolusi hingga 1080p 60fps, sementara sensor belakang bisa sampai 4K 60fps. Dalam kondisi pencahayaan berlimpah, menurut saya kamera tablet Apple satu ini pas untuk digunakan sebagai kamera sekunder. Punya detail dan warna yang oke, serta kualitas perekaman audio superior.</p>


                <p class="text-white-300 font-bold mb-4" style="font-size:20px">Fitur</p>
                <img alt="Gaming setup with a monitor displaying a game, a gaming PC with RGB lighting, and gaming accessories" class="w-full mb-4" height="400" src="<?= base_url('css/img/f_ipadm2.png') ?>" style="width: auto;height:auto;border-radius:2%;object-fit:content" />
                <p class="text-white-300 mb-4" style="text-align: justify;">Terakhir kali saya menggunakan laptop untuk harian adalah iPad 2 rilisan 2011 lalu, jadi saya sangat excited untuk eksplorasi apa saja perkembangan yang sudah dihadirkan lewat iPadOS saat ini. Sayangnya ketika WWDC 2024 kemarin digelar, iPadOS 18 masih memiliki kemampuan multitasking serupa versi sebelumnya. Overall, bagi saya, masih jauh di bawah kemampuan macOS dalam hal menjalankan beragam aplikasi serta fleksibilitas masing-masing.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Memang, iPad Air M2 kini sudah mendukung Stage Manager, sudah bisa jalankan beragam aplikasi sekaligus dalam ukuran jendela kecil yang bisa diatur sesuai selera, bahkan bisa dicolok ke monitor dan berfungsi selayaknya PC dengan mouse dan keyboard. Tapi tetap saja, hal-hal kecil seperti pengaturan window masing-masing aplikasi, akses ke manajer file, sampai kursor digital yang terkadang masih belum kompatibel dengan beberapa situs tertentu.</p>
                <img alt="Gaming setup with a monitor displaying a game, a gaming PC with RGB lighting, and gaming accessories" class="w-full mb-4" height="400" src="<?= base_url('css/img/f_ipadm2_1.png') ?>" style="width: auto;height:auto;border-radius:2%;object-fit:content" />
                <p class="text-white-300 mb-4" style="text-align: justify;">Hal-hal kecil tersebut bila ditemukan dalam keseharian saat mencoba menjajal iPad Air M2, terasa sedikit memperlambat workflow, dibandingkan ketika menggunakan MacBook. Meski memang, yang saya alami, bisa sangat berbeda dengan Gizmo friends, yang punya keseharian akses aplikasi berbeda. Saya bisa melihat potensi iPad Air M2 yang lebih maksimal.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Semisal, kalau friends terbiasa mengakses satu aplikasi saja dalam satu waktu, bakal tidak ada masalah. Banyak edit video? Ukuran layar besar milik iPad Air M2 13 inci, ditambah dukungan input sentuh sampai stylus, bakal mempercepat workflow karena bisa langsung menyentuh bagian footage secara presisi.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Untuk perangkat hiburan, iPad Air M2 dilengkapi speaker stereo yang sangat menggelegar—mungkin karena saya menjajal versi 13 inci, kemungkinan speaker-nya memang lebih superior dari versi 11 inci. Sayangnya, tidak ada jack audio 3,5mm di sisi perangkat. Juga tidak ada Face ID, di mana Apple sediakan Touch ID sebagai alternatif buka kunci layar lewat pelacakan sidik jari dari tombol power.</p>

                <p class="text-white-300 font-bold mb-4" style="font-size:20px">Performa</p>
                <img alt="Gaming setup with a monitor displaying a game, a gaming PC with RGB lighting, and gaming accessories" class="w-full mb-4" height="400" src="<?= base_url('css/img/p_ipadm2.png') ?>" style="width: auto;height:auto;border-radius:2%;object-fit:content" />
                <p class="text-white-300 mb-4" style="text-align: justify;">Jadi salah satu nilai jual utama, iPad Air M2 atau iPad Air 2024 ini hadir dengan cip Apple M2, alias sama persis dengan cip yang digunakan pada MacBook. Menurut saya ini menjadi daya saing yang sangat kuat, dengan skor benchmark superior dibandingkan semua tablet Android yang ada saat ini.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Walaupun iPad Pro terbaru sudah pakai cip M4, Apple M2 punya peningkatan performa signifikan dibandingkan M1. Sementara dari M2 ke M3, tergolong minor. Sehingga menurut saya pilihan tepat untuk iPad Air M2 hadir dengan cip satu ini, yang tentu bakal membuatnya future-proof. Dipasangkan dengan layar yang “hanya” 60Hz, tentu performanya terasa sangat mulus.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Membawa kapasitas RAM 8GB, multitasking berjalan cukup lancar. Sejatinya, bila Gizmo friends membandingkannya dengan iPad Pro M1 atau bahkan iPad 10th Gen, tak bakal terasa signifikan bedanya. Performa iPad Air M2 akan terasa cepat saat mengakses aplikasi yang benar-benar berat, seperti edit foto atau video, kecepatan render, atau menjalankan judul game yang punya grafis cukup berat.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Dengan banyaknya aplikasi yang lebih optimal untuk iPadOS, iPad Air M2 terasa sangat menyenangkan ketika digunakan untuk akses beragam aplikasi berbeda. Tetap saja, menurut saya, yang membatasi perangkat ini untuk membuka potensi penuh chipset-nya. Tetap tidak bisa se-fleksibel macOS saat ingin menjalankan banyak aplikasi sekaligus.</p>

                <p class="text-white-300 font-bold mb-4" style="font-size:20px">Baterai</p>
                <img alt="Gaming setup with a monitor displaying a game, a gaming PC with RGB lighting, and gaming accessories" class="w-full mb-4" height="400" src="<?= base_url('css/img/b_ipadm2.png') ?>" style="width: auto;height:auto;border-radius:2%;object-fit:content" />
                <p class="text-white-300 mb-4" style="text-align: justify;">Mengingat dimensi fisiknya lebih luas, tentu saja kapasitas baterai iPad Air M2 versi layar 13 inci lebih besar dibandingkan versi 11 inci—meski dugaan saya, daya tahannya bakal mirip-mirip, kecuali dalam situasi tertentu seperti waktu standby. Dengan baterai 9,705 mAh, tablet ini bisa saya gunakan rata-rata dua hari pemakaian.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Tidak seperti smartphone atau tablet dengan layar lebih kecil, umumnya saya menggunakan iPad Air M2 13 inci ketika sedang bekerja, atau sesekali untuk konsumsi konten hiburan. Baterainya terasa sangat awet, mirip seperti MacBook dengan cip Apple terbaru. Bagaimana dengan kecepatan pengisian dayanya? Saya sendiri tidak bisa menemukan informasi presisi terkait dukungan maksimum watt, meski Apple sematkan adaptor 20W seperti charger iPhone.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Mengisi daya 30 menit, bisa menambahkan daya sekitar 30%. Sementara untuk mencapai 100%, iPad Air M2 13 inci membutuhkan waktu hampir 2 jam. Memang sedikit memerlukan waktu ekstra, namun setidaknya tahan lama ketika digunakan, bahkan menurut saya fenomenal ketika standby, seolah tak perlu dinonaktifkan sama sekali.</p>


                <p class="text-white-300 font-bold mb-4" style="font-size:20px">Kesimpulan</p>
                <img alt="Gaming setup with a monitor displaying a game, a gaming PC with RGB lighting, and gaming accessories" class="w-full mb-4" height="400" src="<?= base_url('css/img/ke_ipadm2.png') ?>" style="width: auto;height:auto;border-radius:2%;object-fit:content" />
                <p class="text-white-300 mb-4" style="text-align: justify;">Apakah kehadiran iPad Air M2 13 inci bisa menjadi pengganti laptop yang tepat? Lagi-lagi, tergantung dari aplikasi apa saja yang Gizmo friends gunakan. Pastikan kalau aplikasi-aplikasi tersebut berjalan optimal dan punya fitur sama lengkapnya di iPadOS. Saya pribadi, masih lebih memilih untuk menggunakan MacBook Air.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Bila kita bandingkan keduanya, iPad Air M2 13 inci dijual Rp15,499 juta. Itu baru harga tabletnya saja, ya, dalam kapasitas memori 128GB, belum termasuk Magic Keyboard seharga Rp4 jutaan. Sementara MacBook Air M2 dibanderol Rp16 jutaan, punya port lebih lengkap termasuk jack audio, lebih tipis dan lebih ringan, sehingga overall bisa terasa lebih praktis.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Tapi kalau friends mencari tablet Apple layar besar yang future-proof, tidak ada salahnya melirik iPad Air M2. Dengan chipset yang menurut saya overkill saking kencangnya, tentu tablet ini bakal awet digunakan hingga beberapa tahun mendatang. Atau beli versi 11 incinya saja untuk alternatif sedikit lebih terjangkau.</p>

