                <p class="text-white-300 font-bold mb-4" style="font-size:20px">Performa</p>
                <img alt="Gaming setup with a monitor displaying a game, a gaming PC with RGB lighting, and gaming accessories" class="w-full mb-4" height="400" src="<?= base_url('css/img/p_reno12f.png') ?>" style="width: 900px;height:500px;border-radius:2%;object-fit:content" />
                <p class="text-white-300 mb-4" style="text-align: justify;">OPPO Reno12F 5G dibekali dengan chipset Mediatek Dimensity 6300. Chipset 5G ini dipadukan dengan RAM 12GB yang terbilang besar untuk kelas harga Rp 5 jutaan. Pilihan storage tersedia dalam kapasitas 256GB dan 512GB dengan jenis memori UFS 2.2 yang punya read-write data speed mumpuni.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Dipadukan dengan ColorOS 14, performa OPPO Reno12F 5G kala digunakan sehari-hari saya rasa menyenangkan. Berpindah antar aplikasi dapat dilakukan dengan cepat dan sejumlah aplikasi yang terbuka dapat terus terbuka di background berkat RAM yang besar. Kecepatan mengakses menu juga memuaskan, kegiatan sehari-hari saya dalam mengakses aplikasi messaging dan media sosial terakomodir dengan baik.</p>
                <img alt="Gaming setup with a monitor displaying a game, a gaming PC with RGB lighting, and gaming accessories" class="w-full mb-4" height="400" src="<?= base_url('css/img/p_reno12f_1.png') ?>" style="width: 900px;height:500px;border-radius:2%;object-fit:content" />
