                <p class="text-white-300 mb-4" style="text-align: justify;">OPPO sendiri menjamin kelancaran ColorOS pada Reno12F 5G hingga 50 bulan atau setara dengan 4 tahun sejak diluncurkan. Berarti, smartphone ini dijanjikan tetap akan punya performa yang baik hingga 4 tahun kemudian. Cocok untuk pengguna yang mencari smartphone untuk digunakan dalam jangka waktu lama.</p>

                <p class="text-white-300 font-bold mb-4" style="font-size:20px">Baterai</p>
                <img alt="Gaming setup with a monitor displaying a game, a gaming PC with RGB lighting, and gaming accessories" class="w-full mb-4" height="400" src="<?= base_url('css/img/b_reno12f.png') ?>" style="width: 900px;height:500px;border-radius:2%;object-fit:content" />
                <p class="text-white-300 mb-4" style="text-align: justify;">Seperti umumnya smartphone Android kelas mid-range masa kini, OPPO Reno 12F 5G dilengkapi dengan baterai 5000mAh. Daya tahannya sendiri terbilang memuaskan, mampu digunakan hingga 1.5 hari dengan pemakaian sehari-hari dengan jaringan 4G. Terisi penuh di jam 7 pagi, baterai baru akan menunjukkan indikator low battery pada siang di keesokan harinya. Bila digunakan bermain game dengan durasi 1-2 jam sehari, baterai akan habis sebelum jam tidur malam.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Saat baterai habis, proses charging juga menjadi keunggulan dari OPPO Reno12F 5G. Dengan 45w VOOC fast charging, baterai dapat terisi dari 10% hingga penuh dalam waktu 50 menit saja. Tidak perlu menunggu lama, smartphone dapat kembali digunakan seharian. Ia juga kompatibel dengan protokol fast charging lainnya, namun bila tidak menggunakan adaptor VOOC kecepatannya tidak sampai 45w.</p>

                <p class="text-white-300 font-bold mb-4" style="font-size:20px">Kesimpulan</p>
                <img alt="Gaming setup with a monitor displaying a game, a gaming PC with RGB lighting, and gaming accessories" class="w-full mb-4" height="400" src="<?= base_url('css/img/ke_reno12f.png') ?>" style="width: 900px;height:500px;border-radius:2%;object-fit:content" />
