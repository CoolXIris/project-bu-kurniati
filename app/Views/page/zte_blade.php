                <p class="text-white-300 mb-4" style="text-align: justify;">Walaupun chipset-nya gunakan fabrikasi 28nm, baterai ZTE Blade A35 bisa bertahan kurang lebih seharian penuh. Mengingat ini bukanlah sebuah flagship, tentu intensitas penggunaannya tidak akan sama. Sehingga saat digunakan dengan kebutuhan lebih ringan, masih terasa cukup awet kok. Paling standby time-nya saja yang perlu diperhatikan.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Dan dengan pengisian daya 10W, membutuhkan waktu sedikit ekstra untuk mengisi dari nol hingga penuh, kurang lebih hampir tiga jam. Setidaknya, jenis port sudah USB-C, meski harganya sangat terjangkau. Jadi cukup menggunakan satu kabel saja untuk smartphone dan gadget lainnya, tidak merepotkan saat bepergian.</p>

