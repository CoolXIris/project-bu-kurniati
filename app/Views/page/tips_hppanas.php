                <p class="text-white-300 font-bold mb-3" style="font-size: 20px;">Gunakan Charger dan Kabel yang Sesuai</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Salah satu penyebab utama ponsel menjadi panas saat diisi daya adalah penggunaan charger atau kabel yang tidak sesuai. Charger yang tidak kompatibel dengan ponsel dapat menyebabkan pengisian daya yang berlebihan atau tidak stabil, sehingga ponsel menjadi lebih panas dari biasanya. Pastikan kamu selalu menggunakan charger asli dari ponsel kamu atau charger yang memiliki spesifikasi yang sama. Selain itu, pastikan kabel yang digunakan juga dalam kondisi baik dan tidak rusak.</p>
