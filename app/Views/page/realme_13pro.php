                <p class="text-white-300 font-bold mb-4" style="font-size:20px">Desain</p>
                <img alt="Gaming setup with a monitor displaying a game, a gaming PC with RGB lighting, and gaming accessories" class="w-full mb-4" height="400" src="<?= base_url('css/img/d_r13pro.png') ?>" style="width: 900px;height:500px;border-radius:2%;object-fit:content" />
                <p class="text-white-300 mb-4" style="text-align: justify;">Ingin tampil semakin stylish, desain realme 13 Pro+ 5G kini dibuat lebih segar, dengan desain permukaan bodi belakang terinspirasi dari lukisan khas Claude Monet. Ya, pada opsi warna Monet Gold yang merupakan hero color dari realme 13 Pro+ 5G, smartphone terlihat cukup mewah, ditambah dengan lengkungan di beberapa sisinya.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Dalam menghadirkan desain baru ini, realme menggandeng MFA Boston untuk mereplikasi salah satu karya terbaik dari Claude Monet, yakni lukisan haystacks (tumpukan jerami). Permukaan bodi belakang terasa halus, sedikit berkilau dalam kemiringan tertentu saat terkena cahaya, namun dengan permukaan frosted alias membuatnya terbebas dari sidik jari.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Modul kamera belakang realme 13 Pro+ 5G tetap dibuat sangat besar dan berada di tengah, membawa aksen premium pada sekeliling bezel-nya, dan kini membawa teks cetak “HYPERIMAGE+” yang merupakan algoritma kamera berbasis AI terbaru. Hadir dalam profil tipis kurang dari 8,3mm, dengan bobot 190 gram saja.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Selain opsi Monet Gold, juga ada varian Emerald Green yang menggunakan permukaan kulit vegan bertekstur. Tidak hanya stylish, realme juga telah memperkuat struktur bodi realme 13 Pro+ 5G. Lebih tahan gores, juga lebih tahan jatuh, bahkan dengan klaim tetap tidak penyok dari ketinggian jatuh 1,65 meter. Plus, sertifikasi IP65 yang membuatnya tahan dari debu maupun cipratan air.</p>

                <p class="text-white-300 font-bold mb-4" style="font-size:20px">Layar</p>
                <img alt="Gaming setup with a monitor displaying a game, a gaming PC with RGB lighting, and gaming accessories" class="w-full mb-4" height="400" src="<?= base_url('css/img/l_r13pro.png') ?>" style="width: 900px;height:500px;border-radius:2%;object-fit:content" />
                <p class="text-white-300 mb-4" style="text-align: justify;">Selain sisi penampang belakang, tampak depan realme 13 Pro+ 5G juga tetap dirancang stylish, sudah menggunakan panel 3D curved AMOLED berkualitas dengan dimensi 6,7 inci. Karena memliki Armor Shield Glass, alias kaca yang sudah diperkuat.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Panelnya sendiri juga sudah ditingkatkan kecerahannya, kini hingga 2000 nits. Ini membuat layar realme 13 Pro+ 5G tetap terlihat cerah, seperti ketika saya gunakan untuk foto-foto di pantai Kepulauan Seribu saat matahari sedang cerah-cerahnya di siang hari. Tentunya dipadukan dengan profil warna yang pas.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Mendukung 1 milyar warna dan cakupan 100% DCI-P3, ada sejumlah profil warna yang bisa kamu pilih lewat menu Settings. Mulai dari vivid untuk saturasi maksimal yang memanjakan mata, sampai profil khusus untuk akurasi standar P3, supaya lebih pas ketika melihat hasil tangkapan gambar dengan warna akurat. Sisanya sudah sesuai standar flagship, termasuk rasio kontras tinggi dan refresh rate hingga 120Hz.</p>

                <p class="text-white-300 font-bold mb-4" style="font-size:20px">Kamera</p>
                <img alt="Gaming setup with a monitor displaying a game, a gaming PC with RGB lighting, and gaming accessories" class="w-full mb-4" height="400" src="<?= base_url('css/img/k_r13pro.png') ?>" style="width: 900px;height:500px;border-radius:2%;object-fit:content" />
                <p class="text-white-300 mb-4" style="text-align: justify;">Tentu bagian ini menjadi bagian paling menarik dalam artikel review realme 13 Pro+ 5G ini. Secara menyeluruh, realme menyempurnakan kamera Number Series terbarunya, membawa kombinasi hardware terbaik di kelasnya, sekaligus algoritma pemrosesan baru yang disebut banyak memanfaatkan AI. Mari kita bahas terlebih dahulu dari sisi hardware. Apa saja yang ditingkatkan?</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Pertama di segmennya, realme 13 Pro+ 5G mengusung dua sensor Sony LYTIA sekaligus. Sensor utamanya adalah Sony LYT-701, seri terbaru dan pertama di industri, dengan dimensi sensor 1/1,56 inci dan diafragma f/1.88. Sementara itu, sensor periskopnya menggunakan Sony LYT-600. Ya, ketika banyak smartphone kelas menengah gunakan sensor tersebut untuk main sensor, realme memanfaatkannya sebagai kamera periskop 3x optical zoom (hingga 120x digital zoom).</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Kedua sensor tersebut sudah dibekali dengan OIS, sementara sensor ketiga yang berada di belakang adalah 8MP ultra-wide dengan sudut pandang 112 derajat. Di depan, turut disematkan sensor Sony IMX615, beresolusi 32MP dan punya sudut cukup lebar untuk group selfie.</p>
                <img alt="Gaming setup with a monitor displaying a game, a gaming PC with RGB lighting, and gaming accessories" class="w-full mb-4" height="400" src="<?= base_url('css/img/k_r13pro_1.png') ?>" style="width: 900px;height:500px;border-radius:2%;object-fit:content" />
                <p class="text-white-300 mb-4" style="text-align: justify;">Selain hardware, pada sisi pemrosesan gambar, realme 13 Pro+ 5G sudah diperkuat dengan HYPERIMAGE+, yang salah satunya meliputi algoritma kamera terbaru untuk sempurnakan hasil gambar—bahkan membuat smartphone ini menjadi yang pertama, meraih standar baru dari TUV Rheinland untuk sertifikasi kamera beresolusi tinggi dengan kualitas yang terjamin.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Tentu klaim-klaim di atas perlu dibuktikan secara langsung. Overall, kualitas kamera realme 13 Pro+ 5G meningkat, dibandingkan generasi sebelumnya yang sudah memuaskan. Hardware dan software barunya benar-benar sanggup untuk membuat tangkapan gambar menjadi lebih baik.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Mulai dari reproduksi warna, tingkat noise yang lebih terkendali dalam kondisi low-light, pencahayaan pada area wajah yang tetap pas walaupun dalam kondisi backlight, dan skin tone yang kini semakin sempurna—pas, tajam, dengan warna kulit lebih akurat. Kualitas zoom hingga rentang 6-10x juga masih sangat usable.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Menggunakan kamera realme 13 Pro+ 5G untuk menangkap foto portrait terasa sangat menyenangkan. Separasi antar obyek utama dengan latar terlihat mulus dan rapi hingga masing-masing helai rambut. Dan ketika pencahayaan minim, mode portrait otomatis mengaktifkan mode malam, sehingga pengguna hanya perlu fokus ambil foto saja.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Baik sensor utama dan periskop realme 13 Pro+ 5G, bisa merekam video dalam resolusi 4K 30fps maupun 1080p 60fps. Namun untuk footage yang lebih stabil, dalam resolusi 1080p 30fps bisa aktif baik OIS dan EIS, serta bisa berpindah antar sensor saat perekaman berlangsung, termasuk sensor ultra-wide.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Kualitas footage video bisa diandalkan, dengan warna yang memuaskan, termasuk dari sensor periskop—sensor satu ini bisa hasilkan depth of field lebih dalam, sehingga ketika dikombinasikan dengan mode Movie dalam rasio 21:9, memberikan efek video sinematik yang menarik</p>


                <p class="text-white-300 font-bold mb-4" style="font-size:20px">Fitur</p>
                <img alt="Gaming setup with a monitor displaying a game, a gaming PC with RGB lighting, and gaming accessories" class="w-full mb-4" height="400" src="<?= base_url('css/img/f_r13pro.png') ?>" style="width: 900px;height:500px;border-radius:2%;object-fit:content" />
                <p class="text-white-300 mb-4" style="text-align: justify;">realme 13 Pro+ 5G hadir membawa realme UI 5.0 berbasis Android 14. Bagaimana dengan masa depan software-nya? realme sendiri menjanjikan setidaknya bakal ada dua kali pembaruan versi OS, dan tiga tahun pembaruan keamanan rutin. Ada sejumlah fitur AI menarik yang disematkan pada smartphone terbaru satu ini—termasuk diturunkan dari seri realme GT.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Yakni AI Smart Loop yang bisa membantu kamu melakukan proses multitasking lebih cepat, secara pintar memprediksi aplikasi apa yang bakal diakses berikutnya. Lalu ada dua fitur spesial pada editor foto, yakni AI Smart Removal dan AI Ultra Clarity. Sesuai namanya, AI Smart Removal memungkinkan kamu menghapus obyek dan mengisinya secara sangat pintar. Algoritmanya benar-benar sudah sangat matang.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Sementara untuk AI Ultra Clarity, fitur ini berfungsi untuk memperjelas foto yang sedikit buram. Selain dari kamera smartphone (mungkin ketika diambil saat tangan sedang bergetar), juga bisa Gizmo friends manfaatkan untuk merestorasi foto lama. Fitur ini masih dalam tahap Beta, dan realme bakal terus meningkatkan kemampuannya di masa mendatang.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Opsi Air Gestures juga hadir di realme 13 Pro+ 5G untuk mengendalikan sejumlah fungsi smartphone hanya dengan gestur tangan, menghadap kamera depan, pas untuk menerima/menutup panggilan saat tangan sedang kotor. Sisanya seperti vibration motor maupun setup speaker stereo-nya sudah tergolong memuaskan untuk smartphone di kelasnya.</p>

                <p class="text-white-300 font-bold mb-4" style="font-size:20px">Performa</p>
                <img alt="Gaming setup with a monitor displaying a game, a gaming PC with RGB lighting, and gaming accessories" class="w-full mb-4" height="400" src="<?= base_url('css/img/p_r13pro.png') ?>" style="width: 900px;height:500px;border-radius:2%;object-fit:content" />
                <p class="text-white-300 mb-4" style="text-align: justify;">realme tetap menunjuk salah satu chipset yang populer dari Qualcomm, yakni Snapdragon 7s Gen 2, untuk mentenagai realme 13 Pro+ 5G. Karena masih relatif baru, cip satu ini diklaim sudah dibekali dengan NPU yang pas untuk pemrosesan AI—sebuah poin penting, mengingat algoritma kamera realme 13 Pro+ 5G kini lebih kompleks dengan AI.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Sisi positifnya, chipset yang satu ini telah menggunakan proses fabrikasi 4nm, sehingga sudah efisien daya. Dipasangkan dengan RAM lega hingga 12GB LPDDR4x, plus penyimpanan 512GB UFS 3.1, performa realme 13 Pro+ 5G sudah bisa dibilang cukup nyaman untuk penggunaan harian. Mulai dari skenario ringan sampai multitasking, terasa lumayan gegas, meski sesekali saja ada lag, tak begitu mengganggu.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Untuk membuat performa tetap terjaga, disematkan 3D VC Cooling System hingga 9 lapis dan cakupan seluas 4,500mm persegi. Bodi smartphone memang relatif stabil—terasa panas ketika benar-benar akses kamera secara intensif dalam waktu lama saja. Selebihnya seperti mengaktifkan hotspot, tidak hangat. Kecuali saat bermain game, yang juga sangat wajar.</p>

                <p class="text-white-300 font-bold mb-4" style="font-size:20px">Baterai</p>
                <img alt="Gaming setup with a monitor displaying a game, a gaming PC with RGB lighting, and gaming accessories" class="w-full mb-4" height="400" src="<?= base_url('css/img/b_r13pro.png') ?>" style="width: 900px;height:500px;border-radius:2%;object-fit:content" />
                <p class="text-white-300 mb-4" style="text-align: justify;">Menariknya, dibandingkan generasi sebelumnya, kapasitas baterai realme 13 Pro+ 5G dibuat sedikit meningkat, di mana kini menjadi 5,200 mAh. Padahal, secara dimensi tidak ada perbedaan alias kurang lebih sama. Tentunya membuat penggunaan semakin nyaman, tak perlu khawatir daya cepat habis.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Pada hari di mana saya lebih banyak menggunakan kamera realme 13 Pro+ 5G, baterainya tetap sanggup bertahan dari pagi sampai keesokan paginya, tanpa perlu mengaktifkan mode hemat daya sekalipun. Sehingga bila memang digunakan secara lebih ringan, atau memanfaatkan mode baterai tambahan, tentu realme 13 Pro+ 5G bisa bertahan hingga dua hari secara mudah.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Walaupun kapasitas baterai realme 13 Pro+ 5G semakin besar, pengisian dayanya tetap berjalan instan. Karena sudah dipasangkan bersama dukungan 80W SUPERVOOC, plus adaptor charger yang sudah tersedia dalam paket penjualan. 30 menit mengisi daya, sudah bisa menambah daya dari hampir habis menjadi kisaran 75%. Sementara ketika mengisi sampai penuh, butuh waktu kurang lebih 50 menit.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">realme juga berani mengklaim bila kapasitas baterainya bakal tetap lebih dari 80% setelah 1600 siklus—umumnya diartikan menjadi ekuivalen penggunaan normal selama empat tahun. Mode smart charging hadir untuk mempelajari kebiasaan pengisian daya secara cermat. Ingin batasi pengisian hingga 80% saja? Ada opsi khusus yang bisa diaktifkan di menu Settings, bila memang diperlukan.</p>


                <p class="text-white-300 font-bold mb-4" style="font-size:20px">Kesimpulan</p>
                <img alt="Gaming setup with a monitor displaying a game, a gaming PC with RGB lighting, and gaming accessories" class="w-full mb-4" height="400" src="<?= base_url('css/img/ke_r13pro.png') ?>" style="width: 900px;height:500px;border-radius:2%;object-fit:content" />
                <p class="text-white-300 mb-4" style="text-align: justify;">Klaim realme untuk membawa peningkatan yang signifikan terutama pada aspek kamera, terbukti mampu menghasilkan pengalaman fotografi yang memuaskan. Mudah bagi saya untuk bilang kalau realme 13 Pro+ 5G menawarkan kualitas foto terbaik di kelasnya saat ini, terutama lewat kombinasi dua sensor Sony LYTIA.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Fitur AI yang dibawa juga tidak sekadar gimmick. Semua dikemas dalam desain yang stylish, membawa kapasitas baterai lebih besar, dan memiliki banderol harga yang masih kompetitif. Sukses memberikan pengalaman fotografi kelas flagship.</p>
