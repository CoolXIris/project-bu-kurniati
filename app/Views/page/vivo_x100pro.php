                    Saat debut Juni lalu, tak bisa dipungkiri kalau saudaranya, X Fold3 Pro terlihat lebih menarik. Namun vivo X100 Pro memiliki daya tarik tersendiri, berkat setup kameranya yang tidak main-main. Meski terhitung dirilis akhir 2023, hingga saat ini, masih mampu mengungguli kualitas kamera flagship lain yang sudah debut resmi sepanjang 2024.
