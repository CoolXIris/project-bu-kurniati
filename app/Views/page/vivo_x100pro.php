                <p class="text-white-300 font-bold mb-4" style="font-size:20px">Desain</p>
                <img alt="Gaming setup with a monitor displaying a game, a gaming PC with RGB lighting, and gaming accessories" class="w-full mb-4" height="400" src="<?= base_url('css/img/x100_2.png') ?>" style="width: 900px;height:500px;border-radius:2%;object-fit:cover" />
                <p class="text-white-300 mb-4" style="text-align: justify;">Stylish, namun terlihat dan terasa bongsor. Itulah impresi singkat yang saya rasakan, ketika menggenggam dan menggunakan vivo X100 Pro selama beberapa pekan. Tentu, saya tidak bisa berekspektasi kalau flagship vivo satu ini bisa tampil tipis dan ringan, mengingat perlu membawa sejumlah komponen hardware termasuk setup sensor kamera yang lebih superior.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Di Indonesia, vivo X100 Pro hanya tersedia dalam satu opsi warna saja, yakni Asteroid Black. Permukaan kaca bodi belakangnya dibuat anti-glare alias bebas bekas sidik jari, dan menampilkan efek glitter—terlihat premium atau tidak, tentunya subyektif. Ketebalannya sekitar 9mm, dengan bobot sekitar 221 gram. Masih lebih ringan dari Galaxy S24 Ultra, namun terasa lebih berat. Kok bisa?</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Menurut saya, ini karena distribusi bobotnya yang cenderung lebih berat di bagian atas—dugaan mengacu ke peletakkan modul kameranya. Selain terasa berat, bodinya juga sangat licin—lebih licin dari V30/V40, sama licinnya seperti vivo V29. Untuk pengguna dengan telapak tangan cenderung berkeringat seperti saya, terasa kurang nyaman memegang vivo X100 Pro berlama-lama tanpa case dalam genggaman.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Secara keseluruhan, desain vivo X100 Pro terlihat mewah dan premium untuk sebuah flagship rilisan 2024. Hanya saja bukan yang paling tipis atau paling nyaman dalam genggaman, meski membawa kombinasi hardware terbaik di kelasnya.</p>

                <p class="text-white-300 font-bold mb-4" style="font-size:20px">Layar</p>
                <img alt="Gaming setup with a monitor displaying a game, a gaming PC with RGB lighting, and gaming accessories" class="w-full mb-4" height="400" src="<?= base_url('css/img/x100_3.png') ?>" style="width: 900px;height:500px;border-radius:2%;object-fit:cover" />
                <p class="text-white-300 mb-4" style="text-align: justify;">Tampilan premium di sisi depan vivo X100 Pro, tercermin berkat penggunaan panel kaca yang memiliki lengkungan 3D di kedua sisi sampingnya. Hal ini membuat diameter layar 6,78 inci, terasa lebih ramping saat digenggam—terutama ketika dibandingkan dengan Galaxy S24 Ultra, atau iQOO 12 sekalipun. Dan tidak menimbulkan masalah sama sekali, seperti ghost touch maupun lainnya.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Panel yang digunakan tentu sudah flagship-grade; LTPO AMOLED, 1 milyar warna, refresh rate maksimum 120Hz, serta kecerahan maksimum hingga 3000 nits. Membuat vivo X100 Pro tetap nyaman digunakan, termasuk saat hunting foto dalam kondisi siang hari. Viewfinder kamera bisa terlihat jelas dan pengguna bisa tetap fokus mengambil momen.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Dukungan konten HDR dari platform streaming favorit, tentunya sudah hadir lewat vivo X100 Pro. Dan untuk “memamerkan” layarnya, tersedia cukup banyak opsi always-on display yang bisa dipilih, beberapa di antaranya memberikan efek transisi menarik. Sensor sidik jari in-display cukup reliabel—meski menurut saya posisinya sedikit terlalu ke bawah, karena dimensi yang bongsor.</p>

                <p class="text-white-300 font-bold mb-4" style="font-size:20px">Kamera</p>
                <img alt="Gaming setup with a monitor displaying a game, a gaming PC with RGB lighting, and gaming accessories" class="w-full mb-4" height="400" src="<?= base_url('css/img/x100_4.png') ?>" style="width: 900px;height:500px;border-radius:2%;object-fit:cover" />
                <p class="text-white-300 mb-4" style="text-align: justify;">Mari kita masuk ke bagian yang paling menarik. Dengan modul yang sangat besar, kamera vivo X100 Pro didukung dengan tiga sensor yang masing-masing tergolong berkualitas, dipasangkan bersama laser autofocus, serta ZEISS T* lens coating yang mampu mengurangi efek glare dan serupa lainnya. ketiga sensor, sama-sama beresolusi 50MP.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Sensor utama menggunakan Sony IMX989 f/1.8 berukuran 1 inci, alias terbesar di smartphone saat ini secara diameter fisik sensor. Sementara sensor ultra-widenya menggunakan Samsung JN1 f/2.0, mendukung autofokus untuk kemampuan makro. Bagaimana dengan telefoto periskopnya? Pakai OV64B berukuran 1/2 inci—sama seperti yang digunakan pada OPPO Find N3, iQOO 12, hingga realme 12 Pro+ 5G.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Namun vivo telah menyempurnakan masing-masing sensor dengan kolaborasinya bersama ZEISS, serta cip vivo V3 yang merupakan komponen hardware untuk sempurnakan kinerja sistem kameranya. Sehingga walaupun sensor periskopnya sama, hasilnya bisa jauh berbeda dibandingkan smartphone lain. Yang saya suka, vivo berikan cukup banyak opsi focal length—memberikan fleksibilitas yang maksimal.</p>
                <img alt="Gaming setup with a monitor displaying a game, a gaming PC with RGB lighting, and gaming accessories" class="w-full mb-4" height="400" src="<?= base_url('css/img/x100_5.png') ?>" style="width: 900px;height:500px;border-radius:2%;object-fit:cover" />
                <p class="text-white-300 mb-4" style="text-align: justify;">Semua yang dibutuhkan, diberikan lewat aplikasi kamera yang serba lengkap. Mulai dari opsi tone warna vibrant hingga lebih natural a-la ZEISS, efek portrait yang mereplikasi banyak lensa ZEISS profesional, sampai mode manual tentunya. Saya juga menyukai opsi long exposure yang memanfaatkan bracketing, alias tetap bisa ditangkap secara handheld tanpa tripod.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Bagaimana dengan hasil fotonya? Saya bisa bilang kalau kamera vivo X100 Pro bisa hasilkan foto terbaik hingga ulasan ini dibuat. Mulai dari sensor ultra-wide hingga periskop, dalam berbagai rentang pencahayaan, sangat bisa diandalkan. Termasuk zoom puluhan kali pun, detail masih sangat baik—pas untuk kebutuhan cropping. Mode makro yang memanfaatkan sensor periskop juga sangat membantu mendapatkan detail sebuah obyek atau makanan tertentu yang ingin di-highlight.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Selain foto, kemampuan perekaman video vivo X100 Pro juga patut diacungi jempol. Sensor utamanya mendukung resolusi maksimum 8K 30fps, sementara untuk ultra-wide, telefoto, hingga kamera selfie, bisa sampai 4K 60fps. Selain warna dan stabilisasi, mode portrait videonya juga tergolong optimal memberikan efek bokeh pada obyek.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Namun kemampuannya untuk menangkap obyek secara jelas dalam rentang zoom jauh, masih belum bisa diaplikasikan dalam mode video. Ketika dibandingkan dengan iPhone 15 Pro Max, misalnya, pada rentang pembesaran 10-20x, iPhone masih bisa hasilkan kualitas footage lebih jelas dan detail, sementara vivo X100 Pro sudah tergolong noisy dan grainy. Ada baiknya untuk tidak melakukan zoom pada video melebihi 10x.</p>

                <p class="text-white-300 font-bold mb-4" style="font-size:20px">Fitur</p>
                <img alt="Gaming setup with a monitor displaying a game, a gaming PC with RGB lighting, and gaming accessories" class="w-full mb-4" height="400" src="<?= base_url('css/img/f_x100.png') ?>" style="width: 900px;height:500px;border-radius:2%;object-fit:cover" />
                <p class="text-white-300 mb-4" style="text-align: justify;">Bagian pada flagship vivo satu ini yang menurut saya paling lemah, ada di bagian software. Menjalankan FuntouchOS berbasis Android 14, vivo hanya menjanjikan tiga tahun pembaruan perangkat lunak. Jauh di bawah flagship rata-rata seperti Samsung, Apple, sampai Google. Bahkan smartphone kelas menengah Samsung bisa memberikan hingga 4 versi OS baru.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Selain durasi pembaruan, tampilan antarmukanya juga menurut saya terasa sedikit membosankan dan kurang stand-out. Mungkin bakal terasa menyenangkan bila Gizmo friends memang ingin pendekatan UI mendekati stock Android. Namun bila dibandingkan dengan One UI atau ColorOS terbaru, terasa kurang intuitif. Begitu pula dengan fitur AI yang ditawarkan, jauh lebih minim.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Tak seperti vivo V40 Series, fitur seperti menghapus obyek pada gambar di vivo X100 Pro, seolah masih belum didukung AI, karena berkemampuan jauh lebih inferior. Memang, di negara asalnya sudah dirilis sejak lama. Namun setidaknya diperbarui dengan fitur relevan supaya masih layak menjadi pilihan—seperti yang Samsung lakukan dengan menghadirkan Galaxy AI ke flagship beberapa generasi sebelumnya.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Untuk sektor audio, setup speaker stereonya sudah cukup menggelegar, meski bukan yang paling spesial. vivo X100 Pro juga memiliki sensor inframerah yang berada di sisi atas, dan dilengkapi aplikasi untuk mengendalikan perangkat rumah seperti TV maupun AC.</p>

                <p class="text-white-300 font-bold mb-4" style="font-size:20px">Performa</p>
                <img alt="Gaming setup with a monitor displaying a game, a gaming PC with RGB lighting, and gaming accessories" class="w-full mb-4" height="400" src="<?= base_url('css/img/p_x100.png') ?>" style="width: 900px;height:500px;border-radius:2%;object-fit:cover" />
                <p class="text-white-300 mb-4" style="text-align: justify;">Walaupun pertama kali diperkenalkan sejak akhir tahun lalu, vivo X100 Pro terbilang sudah membawa cip terbaik dari MediaTek. Yakni seri Dimensity 9300 dengan fabrikasi 4nm—memang ada versi Plus, namun hanya berbeda clock speed saja, dan tetap bisa hasilkan skor Antutu melampaui 2 juta poin. Selebihnya masih sama, dipasangkan dengan kapasitas RAM dan penyimpanan internal lega.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">vivo dikenal mampu “menjinakkan” berbagai jenis cip MediaTek, terbukti dari sejumlah vivo V Series yang bisa hasilkan kualitas kamera terbaik di kelasnya, “walaupun” pakai MediaTek (terbukti pula di vivo X100 Pro). Untuk kelas flagship akhir 2024 sekalipun, performanya terbilang sangat mulus. Mulai dari multitasking, sampai menjajal sejumlah judul game populer.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Suhu perangkat secara keseluruhan juga cukup terjaga, jarang panas meski akses fitur kamera hingga menyalakan hotspot sepanjang hari. Rasanya, benefit dari bodi yang bukan-paling-tipis, memberikan ruang dan sistem pendingin optimal pada bagian ini. Bisa berpengaruh positif terhadap performa sehari-hari tentunya.</p>

                <p class="text-white-300 font-bold mb-4" style="font-size:20px">Baterai</p>
                <img alt="Gaming setup with a monitor displaying a game, a gaming PC with RGB lighting, and gaming accessories" class="w-full mb-4" height="400" src="<?= base_url('css/img/b_x100.png') ?>" style="width: 900px;height:500px;border-radius:2%;object-fit:cover" />
                <p class="text-white-300 mb-4" style="text-align: justify;">vivo memang telah mengembangkan teknologi baterai yang memungkinkan kapasitas lebih besar dalam dimensi setara. Dan dalam dimensinya yang cukup bonsgor, baterai vivo X100 Pro bisa dibilang di atas rata-rata. Yakni mencapai 5,400 mAh, alias tergolong cukup besar.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Berkat chipset yang sudah efisien daya, baterai vivo X100 Pro sangat bisa diandalkan untuk penggunaan seharian penuh—termasuk ketika mengaktifkan hotspot sampai banyak jepret foto sekalipun, masih bisa bertahan sampai keesokan paginya. Dengan penggunaan lebih ringan, tentu bisa mencapai dua hari secara mudah.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Baterai awet tentunya pas kalau dipasangkan dengan pengisian daya instan. Lewat dukungan 100W fast charging, mengisi daya vivo X100 Pro 15 menit, sudah bisa mencapai kisaran 60%. Sementara untuk mencapai 100%, sedikit melebihi 30 menit saja. Kabar baiknya, charger 120W yang disematkan ke dalam paket penjualan, mendukung standar USB-PD. Sehingga kompatibel dengan banyak perangkat lain.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Tidak lupa, vivo X100 Pro juga sudah mendukung wireless charging hingga 50W, dan reverse wireless charging. Ketika dibutuhkan, bisa diubah sebagai pengisi daya darurat untuk AIoT seperti smartwatch, TWS, sampai iPhone sekalipun—meski kecepatannya tentu saja sangat lambat. Setidaknya tergolong multifungsi. Menjadi nilai plus dibandingkan versi non-Pro.</p>

                <p class="text-white-300 font-bold mb-4" style="font-size:20px">Kesimpulan</p>
                <img alt="Gaming setup with a monitor displaying a game, a gaming PC with RGB lighting, and gaming accessories" class="w-full mb-4" height="400" src="<?= base_url('css/img/ke_x100.png') ?>" style="width: 900px;height:500px;border-radius:2%;object-fit:cover" />
                <p class="text-white-300 mb-4" style="text-align: justify;">Walaupun hadir terlambat, kualitas kamera vivo X100 Pro termasuk salah satu yang terbaik saat ini. Tiga sensor yang disematkan pada modul kamera belakang, saling melengkapi untuk banyak jenis momen yang ingin kamu abadikan setiap harinya. Lengkap dengan tone, filter, hingga efek portrait dari ZEISS yang eksklusif. Yang kurang menurut saya ada pada sektor software, masih belum memiliki fitur AI se-menggoda kompetitornya.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Pun kalau dirasa masih terlalu mahal, ada vivo X100 yang hadir membawa kualitas sensor utama setara, dan sensor periskop yang sama persis, juga bisa menjadi alternatif bila Gizmo friends benar-benar menginginkan kualitas foto terbaik. Semoga generasi berikutnya bisa hadir lebih cepat dari pengenalan perdana di negara asal maupun global, agar konsumen bisa menjajal kehebatan flagship vivo dalam waktu yang lebih cepat.</p>

