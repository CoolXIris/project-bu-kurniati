                    Ya, sesuai dengan judul di atas, kehadiran vivo X100 Pro di Indonesia bisa dibilang sangat terlambat. Dibandingkan Samsung yang bisa adakan pemesanan tepat saat peluncuran, flagship vivo satu ini baru hadir berbulan-bulan setelah pengenalan perdananya akhir 2023 lalu.
