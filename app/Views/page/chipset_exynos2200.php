                <p class="text-white-300 mb-4" style="text-align: justify;">Saat pertama kali debut, cip Exynos 2200 telah dinanti oleh para gamers atau mereka yang doyan main game di smartphone. Pasalnya, Samsung menggandeng AMD untuk memberikan pengalamn gaming terbaik di masanya. Sehingga meski sudah dirilis lebih dari dua tahun lalu, kemampuan grafisnya masih tergolong jempolan. Bisa jalankan sejumlah judul gim berat masa kini.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Modem yang dimiliki cip Exynos 2200, juga mendukung jaringan 5G dengan kecepatan unduh maksimum sampai 7,35Gbps, versus hingga 3,27Gbps pada modem 5G milik cip Dimensity 7300. Aspek lain yang diungguli Exynos 2200, ada pada bagian kompabilitas layar hingga perekaman video, masing-masing bisa sampai QHD+ 144Hz dan 8K30fps (vs FHD+ 144Hz & 4K30fps).</p>

                <p class="text-white-300 font-bold mb-3" style="font-size: 20px;">Kemampuan Pemrosesan AI Dimensity 7300 Tidak Kalah Oke</p>
                <img alt="Gaming setup with a monitor displaying a game, a gaming PC with RGB lighting, and gaming accessories" class="w-full mb-4 mx-auto" height="400" src="<?= base_url('css/img/2200_3.png') ?>" style="width: 900px;height:500px;border-radius:2%;object-fit:cover;" />
                <p class="text-white-300 mb-4" style="text-align: justify;">Karena rilis jauh lebih baru dibandingkan Exynos 2200, cip MediaTek Dimensity 7300 masih terbilang punya sejumlah keunggulan tersendiri. Terkait efisiensi daya, situs Nanoreview memberikan skor lebih tinggi untuk Dimensity 7300 (91 poin) dibandingkan Exynos 2200 (86 poin). Besar kemungkinan berkat fabrikasi TSMC yang dikenal lebih efisien dari milik Samsung, serta TDP lebih rendah. Lalu bagaimana dengan kemampuan pemrosesan AI-nya?</p>
                <img alt="Gaming setup with a monitor displaying a game, a gaming PC with RGB lighting, and gaming accessories" class="w-full mb-4 mx-auto" height="400" src="<?= base_url('css/img/2200_4.png') ?>" style="width: auto;height:auto;border-radius:2%;object-fit:auto;" />
