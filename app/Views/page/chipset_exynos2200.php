                <p class="text-white-300 font-bold mb-3" style="font-size: 20px;">Arsitektur CPU & Performa Exynos 2200 Lebih Unggul</p>
                <img alt="Gaming setup with a monitor displaying a game, a gaming PC with RGB lighting, and gaming accessories" class="w-full mb-4 mx-auto" height="400" src="<?= base_url('css/img/2200_1.png') ?>" style="width: 900px;height:500px;border-radius:2%;object-fit:cover;" />
                <p class="text-white-300 mb-4" style="text-align: justify;">Pada sektor performa, baik Exynos 2200 dan Dimensity 7300 memiliki sejumlah kesamaan. Keduanya sama-sama menggunakan proses fabrikasi 4nm, yang bisa dibilang hemat daya. Arsitektur CPU-nya pun sama-sama 8 inti, namun cip rilisan Samsung membawa tiga jenis CPU berbeda, dengan clock speed yang juga lebih tinggi.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Kalau Dimensity 7300 membawa empat inti Cortex-A78 2,5GHz dan empat inti Cortex-A55 2GHz, Exynos 2200 cenderung lebih superior—sangat wajar, karena peruntukannya adalah hadir sebagai cip flagship terbaik. Ada satu inti Cortex-X2 hingga 2,8GHz, dengan tiga inti Cortex-A710 2,52GHz dan empat inti Cortex A510 1,82GHz.</p>
