                <p class="text-white-300 mb-4" style="text-align: justify;">Mouse gaming Viper V3 Pro Sentinels Edition dilengkapi dengan teknologi tercanggih dari Razer yang memastikan performa tanpa kompromi. Mengusung sensor optik Razer Focus Pro 35K Gen-2, mouse ini memberikan akurasi resolusi hingga 99,8%. Sensor ini dapat diatur melalui konfigurasi perangkat lunak canggih yang memungkinkan pengguna mengoptimalkan sensitivitas dan rotasi mouse sesuai dengan preferensi mereka, menjadikannya pilihan ideal bagi para gamer profesional.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Selain itu, mouse ini juga mendukung polling rate hingga 8000 Hz melalui Razer HyperPolling Wireless Dongle, yang menjamin latency rendah untuk performa puncak di setiap sesi permainan. Mouse ini dirancang dengan bobot yang sangat ringan, hanya 54 gram, memungkinkan kenyamanan dan kelincahan saat digunakan dalam waktu lama. Bahan dan desain Viper V3 Pro yang ergonomis memastikan pengguna tidak cepat lelah, bahkan dalam sesi permainan yang panjang dan intens.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Fitur-fitur canggih lainnya meliputi Razer Optical Mouse Switches Gen-3 dengan siklus klik tahan lama hingga 90 juta klik. Ini memastikan keandalan dan daya tahan tinggi yang sangat diperlukan bagi gamer profesional yang bermain dalam tempo cepat. Selain itu, mouse ini juga didesain untuk efisiensi daya, memberikan waktu penggunaan hingga 95 jam, memungkinkan pengguna bermain tanpa harus sering-sering mengisi ulang daya.</p>


