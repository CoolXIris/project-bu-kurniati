                <p class="text-white-300 mb-4" style="text-align: justify;">Harga WD My Passport 6TB dibanderol Rp2,950 juta, namun di e-commerce ada cukup banyak toko yang menjualnya seharga Rp2,7 jutaan. Kalau dibandingkan dengan penyimpanan eksternal berjenis SSD, kapasitas yang didapat hanya 2TB, alias sepertiganya. Tentu WD My Passport 6TB bakal terlihat jauh lebih menggiurkan bagi mereka yang benar-benar membutuhkan kapasitas penyimpanan lega.</p>
                <p class="text-white-300 mb-4" style="text-align: justify;">Terkait daya tahan HDD vs SSD, meski SSD diklaim bisa sampai dua kali lipat lebih tahan lama, hal tersebut juga sangat bergantung dari skenario pemakaian; intensitas penggunaan, sampai suhu pengoperasian. Untuk sekadar fungsi backup, WD My Passport 6TB tentu sudah sangat pas bagi penggunaan dalam waktu cukup lama.</p>

